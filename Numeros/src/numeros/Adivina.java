    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        // TODO add your handling code here:
         
        int txt = Integer.parseInt(txtIngreso.getText());
         
         
        if ( txt == aleatorio){
             JOptionPane.showMessageDialog(rootPane, "ADIVINASTE!!");
             txtIngreso.setEnabled(false);             
        }if (txt > (aleatorio-50)) {
             getContentPane().setBackground(new java.awt.Color(204,0,0));
             lblMensaje.setText("Se esta acercando");
        }else {
            getContentPane().setBackground(new java.awt.Color(0,102,204));
            lblMensaje.setText("Se esta alejando");
        }
    }//GEN-LAST:event_btnOKActionPerformed

